    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        if (indice > 4) {
            JOptionPane.showMessageDialog(null, "Limite máximo de disciplinas alcançado!");
            return;
        }

        String nome = jtfNome.getText();
        int periodo = Integer.parseInt(jtfPeriodo.getText());
        float notas[] = new float[3];
        notas[0] = Float.parseFloat(jtfNota1.getText());
        notas[1] = Float.parseFloat(jtfNota2.getText());
        notas[2] = Float.parseFloat(jtfNota3.getText());

        if (rbPratica.isSelected()) {
            int laboratorio = Integer.parseInt(jtfLaboratorio.getText());
            float horas = Float.parseFloat(jtfHoras.getText());
            Pratica p = new Pratica(nome, periodo, notas, horas, laboratorio);
            disciplinas[indice] = p;
        } else {
            int sala = Integer.parseInt(jtfSala.getText());
            Teorica t = new Teorica(nome, periodo, notas, sala);
            disciplinas[indice] = t;
        }
        indice++;
        JOptionPane.showMessageDialog(null, "Disciplina cadastrada com sucesso!");
        limparCampos();

        for (int i = 0; i < indice; i++) {
            System.out.println(disciplinas[i].getNome());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed


    private void habilitarCamposEspecificos() {
        jtfLaboratorio.setEditable(rbPratica.isSelected());
        jtfHoras.setEditable(rbPratica.isSelected());
        jtfSala.setEditable(rbTeorica.isSelected());
    }

    private void limparCampos() {
        jtfNome.setText("");
        jtfPeriodo.setText("");
        jtfNota1.setText("");
        jtfNota2.setText("");
        jtfNota3.setText("");
        rbPratica.setSelected(false);
        rbTeorica.setSelected(false);
        jtfHoras.setText("");
        jtfLaboratorio.setText("");
        jtfSala.setText("");
        habilitarCamposEspecificos();
    }

